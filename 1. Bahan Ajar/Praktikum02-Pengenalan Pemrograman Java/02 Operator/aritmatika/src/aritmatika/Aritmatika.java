package aritmatika;
public class Aritmatika {
    public static void main(String[] args) {
        //deklarasi inisialisasi lebih dari 1
        int t=20, p=7;
        System.out.println("Tambah = "+ (t+p));
        System.out.println("Kurang = "+ (t-p));
        System.out.println("Bagi = "+ (t/p));
        System.out.println("Kali= "+ (t*p));
        System.out.println("Modulus= "+ (t%p));
    }
}
