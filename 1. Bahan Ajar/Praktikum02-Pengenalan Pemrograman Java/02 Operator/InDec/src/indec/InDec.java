package indec;
public class InDec {
    public static void main(String[] args) {
        int t = 8, p = 13;
        System.out.println("t = " + t);
        System.out.println("p = " + p);
        System.out.println("t = " + ++t);
        System.out.println("p = " + p++);
        System.out.println("t = " + t--);
        System.out.println("p = " + --p);
    }    
}
